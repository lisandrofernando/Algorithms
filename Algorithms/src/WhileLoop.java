public class WhileLoop {
    
    public static void main(String[] args) {
        
        int i=0;

        int j=10;

        while(i<10){
            System.out.println(i);
            i++;
        }

        do{
            System.out.println(j);
            j++;
        }
        while(j<20);

        //Do while loop at least one loop is guarantee and will execute the do first then check the condition in the while loop

    }
}
